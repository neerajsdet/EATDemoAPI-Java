package week5.session1;

public class PatternPracticeLoops {

    public static void main(String...s){


        /*
        *
        * *
        * * *
        * * * *
        * * * * *
        * * * * * *
        */

        for (int i=0; i<10; i++){
            for (int j=0; j<i; j++ ){
                System.out.print("* ");
            }
            System.out.println("");
        }


    }

}
